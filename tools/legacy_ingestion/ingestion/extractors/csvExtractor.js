// ingestion/extractors/csvExtractor.js
import fs from 'fs';
import csv from 'csv-parser';

export function extractCSV(filePath) {
  return new Promise((resolve, reject) => {
    const rows = [];
    fs.createReadStream(filePath)
      .pipe(csv())
      .on('data', row => rows.push(row))
      .on('end', () => resolve(rows))
      .on('error', reject);
  });
}
